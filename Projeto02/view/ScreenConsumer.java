    private void ButAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButAddActionPerformed
        String idSelected = ListProducts.getSelectedValue();
        if (idSelected != null) {

            // este comando retorna um array onde o sinal " | " é o separador
            int selectedId = Integer.parseInt(idSelected.split("\\|")[0].replace("ID:", "").trim());
            // depois seleciona o id do array, tira a string e eremove os espaços

            String CarProduct = ListProducts.getSelectedValue();

            if (CarProduct != null) {
                model2.addElement(CarProduct); // adiciona o produto no carrinho
                model1.removeElement(CarProduct); // tira o produto do sistema
                ProductController.deleteProduct(selectedId); // deleta o produto do banco de dados
            }
        }
        loadProducts1();
    }//GEN-LAST:event_ButAddActionPerformed

    private void ButRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButRemoveActionPerformed
        String idSelected = ListCar.getSelectedValue();
        if (idSelected != null) {

            // este comando retorna um array onde o sinal " | " é o separador
            // depois seleciona o id do array, tira a string e eremove os espaços
            String selectedProduct = idSelected.split("\\|")[1].replace("Product:", "").trim();
            String selectedValue = idSelected.split("\\|")[2].replace("Value:", "").trim();
            String selectedBrand = idSelected.split("\\|")[3].replace("Brand:", "").trim();
            String selectedKg = idSelected.split("\\|")[4].replace("Kg:", "").trim();

            model2.removeElement(idSelected); // tira o produto do carrinho
            ProductController.registerProduct(selectedProduct, selectedValue, selectedBrand, selectedKg); // adiciona novamente ao banco
            
            loadProducts1();
        }
    }//GEN-LAST:event_ButRemoveActionPerformed

